const teams = {
    gardiens: teamGardiens,
    defenseurs: teamDefenseurs,
    milieux: teamMilieux,
    attaquants: teamAttaquants,
};

function renderTeam(team, containerId) {
    const container = document.getElementById(containerId);
    const cardContainer = container.querySelector('.card-container');
    
    // Index du joueur actuel
    let currentIndex = 0;

    // Fonction pour afficher le joueur actuel
    function displayPlayer(index) {
        // Vider le conteneur avant d'ajouter le nouveau joueur
        cardContainer.innerText = '';

        const player = team[index];

        const card = document.createElement('div');
        card.classList.add('player-card');

        const img = document.createElement('img');
        img.src = player.imageUrl;
        img.alt = player.name;

        const playerInfo = document.createElement('div');
        playerInfo.classList.add('player-info');

        const playerName = document.createElement('h3');
        playerName.textContent = player.name;

        const playerNumber = document.createElement('p');
        playerNumber.textContent = `Numéro : ${player.numeroEquipe}`;

        const playerAge = document.createElement('p');
        playerAge.textContent = `Âge : ${player.age} ans`;

        const playerNationality = document.createElement('p');
        playerNationality.textContent = `Nationalité : ${player.nationalité}`;

        const nextButton = document.createElement('button');
        nextButton.classList.add('next-button', 'no-style');
        nextButton.textContent = 'Suivant';
        nextButton.onclick = () => {
            currentIndex = (currentIndex + 1) % team.length; // Passer au joueur suivant
            displayPlayer(currentIndex); // Afficher le joueur suivant
        };

        playerInfo.appendChild(playerName);
        playerInfo.appendChild(playerNumber);
        playerInfo.appendChild(playerAge);
        playerInfo.appendChild(playerNationality);
        card.appendChild(img);
        card.appendChild(playerInfo);
        card.appendChild(nextButton);
        cardContainer.appendChild(card);
    }

    // Afficher le premier joueur au chargement
    displayPlayer(currentIndex);
}

// Appel de la fonction pour chaque équipe
renderTeam(teams.gardiens, 'gardiens');
renderTeam(teams.defenseurs, 'defenseurs');
renderTeam(teams.milieux, 'milieux');
renderTeam(teams.attaquants, 'attaquants');
